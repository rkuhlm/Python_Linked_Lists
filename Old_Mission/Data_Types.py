# Floating Points (float) = Numbers with a decimal point EX: 2.2 5.8 100.0
# Strings (str) = Order sequence of characters EX: "hello" "200" "Sammy"
# Lists (list) = Ordered sequence of objects EX: [10, "hello", 20.3]
# Dictionaries (dict) = Unordered Key Value Pairs EX: {"MyKey1" : "Value", "MyKey2" : "Value2"}
# Tuples (tup) = Ordered immutable (not changeable) sequence of objects EX: (10, "hello", 200.3)
# Sets (set) = Unordered collection of unique objects EX: {"a", "b"}
# Booleans (bool) = True or False

