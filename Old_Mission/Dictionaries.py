# Dictionaries (dict) = Unordered Key Value Pairs EX: {"MyKey1" : "Value", "MyKey2" : "Value2"}
my_dict = {'Key1': 'Value1', 'Key2': 'Value2'}
print(my_dict)
print("-------------------------------------------------------------------")
print(my_dict['Key1'])
print(my_dict['Key2'])
print("-------------------------------------------------------------------")
prices = {'apple': 2.99, 'orange': 1.99, 'milk': 5.80}
print(prices)
print(prices['apple'])
print("-------------------------------------------------------------------")
